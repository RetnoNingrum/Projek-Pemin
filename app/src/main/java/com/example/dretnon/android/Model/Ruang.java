package com.example.dretnon.android.Model;

public class Ruang {

    String idRuang, Ruang;

    public Ruang(String idRuang, String ruang) {
        this.idRuang = idRuang;
        this.Ruang = ruang;
    }

    public String getIdRuang() {
        return idRuang;
    }

    public void setIdRuang(String idRuang) {
        this.idRuang = idRuang;
    }

    public String getRuang() {
        return Ruang;
    }

    public void setNama(String ruang) {
        this.Ruang = ruang;
    }

}
